    public ViewPastBids(Company company) {
        initComponents();
        DefaultTableModel dfm=(DefaultTableModel) table.getModel();
        ArrayList<String> bidders=company.getBidders_list();
        ArrayList<Double> bids=company.getBid_list();
        
        String client_name=null;
        double bid_price;
        
        for(int i=0;i < bids.size();i++){
            client_name=bidders.get(i);
            bid_price=bids.get(i);
            dfm.addRow(new Object[]{client_name,bid_price});
            
        }
        setLocationRelativeTo(this);
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);
    }

    
    

